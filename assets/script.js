//1. Defines the environment with variables for the Generate Button and character set
var generateBtn = document.querySelector("#generate");
var charset = ' ';

//4. Randomly generates a combination of characters by providing the user with a series of criteria to determine the length and substance of the passwords
//4. Each confirm window passess a boolean value that indicates whether to include a specific set of characters in the 'charset' variable
function generatePassword(passwordLength, passwordCaps, passwordLower, passwordNumeric, passwordSpecial) {
  passwordLength = prompt("Please enter the number of characters for your password (min. 8 - max. 128)");
  if (passwordLength > 128 ||passwordLength < 8) {
    writePassword(); 
  } else {
    passwordCaps = confirm("Click OK to include capital letters")
    if (passwordCaps === true) {
      charset += 'ABCDEFGHIJKLMNOPQRSTUVWXYZ'
    };
    passwordLower = confirm("Click OK to include lower case letters");
    if (passwordLower === true) {
      charset += 'abcdefghijklmnopqrstuvwxyz'
    };
    passwordNumeric = confirm("Click OK to include numeric charcters");
    if (passwordNumeric === true) {
      charset += '0123456789'
    };
    passwordSpecial = confirm("Click OK to include special characters");
    if (passwordSpecial === true) {
      charset +='!@#$%^&*()'
    };
  };

  var password = [];
//6. The while loop uses the Math.Floor(Math.random) operation to randomly generate a character from the 'charset' variable until the number of randomly generated characters matches the 'passwordLength' variable specified by the user 
//6. Locally redefining the variable 'password' as an empty array allows the random characters generated by the Math.floor(Math.random) operator to be stored in the 'password' array
//6. The final return password.join operation joins the individually generated characters in the 'password' array into a string variable
  while (password.length < passwordLength) {
    var char = charset[Math.floor(Math.random() * charset.length)];
    password.push(char);
    console.log(password);
  }
  return password.join('');
};

//3. Triggers the another function to randomly generate a combination of characters to serve as the variable 'password'
//3. Defines that, after completing the generatePassword function, the variable 'password' will be displayed in the HTML textarea element with the id 'password'
function writePassword() {
  var password = generatePassword();
  var passwordText = document.querySelector("#password");
  passwordText.value = password;
};

//2. Gives functionality to the Generate Button by adding an event listener that initiates the 'writePassword' function when clicked
generateBtn.addEventListener("click", writePassword);


